import { useState } from 'react'

function App() {
  const [count, setCount] = useState(0)

 return (
  <> 
   <div>
        <h1>count {count}</h1>
  </div>
  <div>
    <button onClick={() =>setCount(count+1)}>Increment</button>
    <button onClick={() =>setCount(count-1)}>Decrement</button>
    <button onClick={() =>setCount(0)}>Reset</button>
  </div>
  </>
 )
 }

export default App